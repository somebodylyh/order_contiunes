"""
PyTorch Dataset wrapper for Linear Rotation Experiment

Wraps LinearDynamicalGenerator to provide data for training the Agent.
"""

import torch
from torch.utils.data import Dataset
from collections import Counter
import numpy as np
from typing import List, Optional
from .data_generator import LinearDynamicalGenerator


class LinearRotationDataset(Dataset):
    """
    PyTorch Dataset for Linear Rotation Experiment.

    Supports two modes:
    - 'train': Online generation (no memorization)
    - 'eval': Static pre-generated data (for reproducible evaluation)

    Returns:
        {
            'tokens': [L],              # Ground truth time series [t0, t1, ..., t_{L-1}]
            'logical_ids': [L],         # l2r order = [0, 1, 2, ..., L-1]
            'unshuffled_tokens': [L],   # Same as tokens (in time order)
            'order': [L],               # Ground truth order (l2r) = [0, 1, 2, ..., L-1]
            'bag': Counter              # Shuffled multiset (model's input)
        }

    Note:
        - 'tokens' is the sequence generated by the dynamical system (time order)
        - 'logical_ids' = [0,1,2,...,L-1] represents "this is token 0, token 1, ..."
        - Agent's goal: Learn to recover l2r order from bag
        - Training: Model sees bag (shuffled), Agent decides generation order
    """

    def __init__(
        self,
        vocab_size: int = 64,
        seq_length: int = 20,
        hidden_dim: int = 64,
        ortho_mode: str = 'random',
        num_samples: int = 10000,
        seed: int = None,
        mode: str = 'train',
        virtual_size: int = None,
        fixed_R_path: str = None,
        start_tokens: Optional[List[int]] = None,
        generator: Optional[LinearDynamicalGenerator] = None
    ):
        """
        Initialize the dataset.

        Args:
            vocab_size: Vocabulary size V
            seq_length: Sequence length L
            hidden_dim: Hidden dimension D (usually D = V)
            ortho_mode: 'random' or 'permutation'
            num_samples: Number of samples (for eval) or virtual epoch size (for train)
            seed: Random seed for reproducibility
            mode: 'train' (online generation) or 'eval' (static data)
            virtual_size: Virtual epoch size for train mode (defaults to num_samples)
            fixed_R_path: Path to fixed R matrix file
            start_tokens: List of start tokens to use (for fixed R mode)
            generator: Optional shared generator instance (to ensure same R across splits)
        """
        self.vocab_size = vocab_size
        self.seq_length = seq_length
        self.hidden_dim = hidden_dim
        self.ortho_mode = ortho_mode
        self.mode = mode
        self.num_samples = num_samples
        self.virtual_size = virtual_size if virtual_size is not None else num_samples
        self.fixed_R_path = fixed_R_path
        self.start_tokens = start_tokens

        # Use shared generator if provided, otherwise create new one
        if generator is not None:
            self.generator = generator
        else:
            self.generator = LinearDynamicalGenerator(
                vocab_size=vocab_size,
                hidden_dim=hidden_dim,
                ortho_mode=ortho_mode,
                seed=seed,
                fixed_R_path=fixed_R_path
            )

        # Set random seed for start token generation
        if seed is not None:
            np.random.seed(seed)

        # Fixed R mode with start_tokens: pre-generate deterministic sequences
        if fixed_R_path and start_tokens is not None:
            self.samples = []
            print(f"Generating {len(start_tokens)} deterministic sequences from fixed R...")

            for start in start_tokens:
                result = self.generator.generate_sequence(
                    length=seq_length,
                    start_token_id=start,
                    mode='argmax'
                )

                tokens = result['tokens']
                bag = result['bag']

                sample = {
                    'tokens': tokens,
                    'logical_ids': list(range(seq_length)),
                    'unshuffled_tokens': tokens.copy(),
                    'order': list(range(seq_length)),
                    'bag': bag
                }
                self.samples.append(sample)

            print(f"Generated {len(self.samples)} deterministic samples (start tokens: {start_tokens[0]}-{start_tokens[-1]})")

        elif mode == 'eval':
            # Pre-generate static data for evaluation (legacy mode)
            self.samples = []
            print(f"Generating {num_samples} static rotation sequences for evaluation...")

            for i in range(num_samples):
                if (i + 1) % 2000 == 0:
                    print(f"  Progress: {i + 1}/{num_samples}")

                # Random start token
                start_token = np.random.randint(0, vocab_size)

                # Generate sequence
                result = self.generator.generate_sequence(
                    length=seq_length,
                    start_token_id=start_token,
                    mode='argmax'
                )

                tokens = result['tokens']
                bag = result['bag']

                # Create sample
                sample = {
                    'tokens': tokens,  # Ground truth sequence in time order
                    'logical_ids': list(range(seq_length)),  # [0, 1, 2, ..., L-1]
                    'unshuffled_tokens': tokens.copy(),  # Same as tokens
                    'order': list(range(seq_length)),  # Ground truth l2r order
                    'bag': bag  # Multiset for model input
                }

                self.samples.append(sample)

            print(f"Generated {len(self.samples)} static samples")

        elif mode == 'train':
            # Online generation mode - no pre-generation
            print(f"Initialized online generation mode (virtual epoch size: {self.virtual_size})")
            self.samples = None

        else:
            raise ValueError(f"Unknown mode: {mode}. Must be 'train' or 'eval'")

    def __len__(self):
        if self.mode == 'train':
            return self.virtual_size
        elif self.fixed_R_path and self.start_tokens is not None:
            # Fixed R mode: return virtual_size for training, actual size for eval
            return self.virtual_size
        else:  # eval
            return len(self.samples)

    def __getitem__(self, idx):
        """
        Get a sample from the dataset.

        For train mode: Generate on-the-fly
        For eval mode: Return pre-generated sample
        For fixed R mode: Cycle through pre-generated samples

        Returns a dictionary with tensors for training.
        """
        if self.mode == 'train' and self.samples is None:
            # Online generation - create fresh sample every time
            start_token = np.random.randint(0, self.vocab_size)

            result = self.generator.generate_sequence(
                length=self.seq_length,
                start_token_id=start_token,
                mode='argmax'
            )

            tokens = result['tokens']
            bag = result['bag']

            # Create sample
            sample = {
                'tokens': tokens,
                'logical_ids': list(range(self.seq_length)),
                'unshuffled_tokens': tokens.copy(),
                'order': list(range(self.seq_length)),
                'bag': bag
            }

        else:
            # Return pre-generated static sample (cycle through samples if needed)
            actual_idx = idx % len(self.samples)
            sample = self.samples[actual_idx]

        return {
            'tokens': torch.tensor(sample['tokens'], dtype=torch.long),
            'logical_ids': torch.tensor(sample['logical_ids'], dtype=torch.long),
            'unshuffled_tokens': torch.tensor(sample['unshuffled_tokens'], dtype=torch.long),
            'order': torch.tensor(sample['order'], dtype=torch.long),
            'bag': sample['bag']  # Keep as Counter (will be processed by model)
        }

    def get_statistics(self):
        """
        Compute dataset statistics.

        Returns:
            Dictionary with statistics about the dataset
        """
        if self.mode == 'train':
            # For online mode, generate a few samples to compute statistics
            print("  Computing statistics from 100 online samples...")
            samples_to_check = []
            for _ in range(100):
                start_token = np.random.randint(0, self.vocab_size)
                result = self.generator.generate_sequence(
                    length=self.seq_length,
                    start_token_id=start_token,
                    mode='argmax'
                )
                samples_to_check.append({'tokens': result['tokens']})

        else:  # eval mode
            if len(self.samples) == 0:
                return {}
            samples_to_check = self.samples[:min(1000, len(self.samples))]

        # Compute uniqueness rate
        unique_count = 0
        valid_count = 0
        all_margins = []

        for sample in samples_to_check:
            tokens = sample['tokens']

            # Verify uniqueness
            is_valid, is_unique = self.generator.verify_uniqueness(tokens)
            if is_valid:
                valid_count += 1
            if is_unique:
                unique_count += 1

            # Compute margins
            margins = self.generator.compute_margin(tokens)
            all_margins.extend(margins)

        num_checked = len(samples_to_check)

        return {
            'vocab_size': self.vocab_size,
            'seq_length': self.seq_length,
            'num_samples': self.num_samples if self.mode == 'eval' else self.virtual_size,
            'validity_rate': valid_count / num_checked,
            'uniqueness_rate': unique_count / num_checked,
            'avg_margin': np.mean(all_margins),
            'std_margin': np.std(all_margins),
            'min_margin': np.min(all_margins),
            'max_margin': np.max(all_margins)
        }


def create_dataloaders(
    vocab_size: int = 64,
    seq_length: int = 20,
    hidden_dim: int = 64,
    ortho_mode: str = 'random',
    train_samples: int = 8000,
    val_samples: int = 1000,
    test_samples: int = 1000,
    batch_size: int = 64,
    num_workers: int = 0,
    seed: int = 42,
    train_mode: str = 'online',
    fixed_R_path: str = None,
    train_start_tokens: Optional[List[int]] = None,
    val_start_tokens: Optional[List[int]] = None
):
    """
    Create train/val/test dataloaders.

    Args:
        vocab_size: Vocabulary size
        seq_length: Sequence length
        hidden_dim: Hidden dimension
        ortho_mode: Orthogonal matrix mode
        train_samples: Number of training samples (or virtual epoch size for online mode)
        val_samples: Number of validation samples
        test_samples: Number of test samples
        batch_size: Batch size
        num_workers: Number of dataloader workers
        seed: Random seed
        train_mode: 'online' (dynamic generation) or 'static' (pre-generated)
        fixed_R_path: Path to fixed R matrix file (for consistent dynamics)
        train_start_tokens: List of start tokens for train split (for fixed R mode)
        val_start_tokens: List of start tokens for val split (for fixed R mode)

    Returns:
        (train_loader, val_loader, test_loader)
    """
    # Create SHARED generator if using fixed R (ensures same R for all splits)
    shared_generator = None
    if fixed_R_path:
        shared_generator = LinearDynamicalGenerator(
            vocab_size=vocab_size,
            hidden_dim=hidden_dim,
            fixed_R_path=fixed_R_path
        )
        print(f"Created shared generator with fixed R from {fixed_R_path}")

    # Create datasets
    train_dataset = LinearRotationDataset(
        vocab_size=vocab_size,
        seq_length=seq_length,
        hidden_dim=hidden_dim,
        ortho_mode=ortho_mode,
        num_samples=train_samples,
        seed=seed,
        mode='train' if train_mode == 'online' else 'eval',
        virtual_size=train_samples,
        fixed_R_path=fixed_R_path,
        start_tokens=train_start_tokens,
        generator=shared_generator
    )

    val_dataset = LinearRotationDataset(
        vocab_size=vocab_size,
        seq_length=seq_length,
        hidden_dim=hidden_dim,
        ortho_mode=ortho_mode,
        num_samples=val_samples,
        seed=seed + 1000,
        mode='eval',  # Always static for validation
        fixed_R_path=fixed_R_path,
        start_tokens=val_start_tokens,
        generator=shared_generator
    )

    # Test uses val_start_tokens (same sequences as validation)
    test_dataset = LinearRotationDataset(
        vocab_size=vocab_size,
        seq_length=seq_length,
        hidden_dim=hidden_dim,
        ortho_mode=ortho_mode,
        num_samples=test_samples,
        seed=seed + 2000,
        mode='eval',  # Always static for testing
        fixed_R_path=fixed_R_path,
        start_tokens=val_start_tokens,  # Use same start tokens as val for test
        generator=shared_generator
    )

    # Print statistics (only for eval datasets)
    print("\n📊 Dataset Statistics:")
    for name, dataset in [('Val', val_dataset), ('Test', test_dataset)]:
        stats = dataset.get_statistics()
        print(f"\n{name} Dataset:")
        print(f"  Samples: {stats['num_samples']}")
        print(f"  Validity Rate: {stats['validity_rate']*100:.2f}%")
        print(f"  Uniqueness Rate: {stats['uniqueness_rate']*100:.2f}%")
        print(f"  Avg Margin: {stats['avg_margin']:.3f}")

    print(f"\nTrain Dataset:")
    print(f"  Mode: {train_dataset.mode}")
    if train_dataset.mode == 'train':
        print(f"  Virtual Epoch Size: {train_dataset.virtual_size}")
    else:
        print(f"  Static Samples: {len(train_dataset)}")

    # Create dataloaders
    train_loader = torch.utils.data.DataLoader(
        train_dataset,
        batch_size=batch_size,
        shuffle=True,
        num_workers=num_workers,
        pin_memory=True
    )

    val_loader = torch.utils.data.DataLoader(
        val_dataset,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=True
    )

    test_loader = torch.utils.data.DataLoader(
        test_dataset,
        batch_size=batch_size,
        shuffle=False,
        num_workers=num_workers,
        pin_memory=True
    )

    return train_loader, val_loader, test_loader


