def test_margin_computation():
    """Test margin computation"""
    print("Test 4: Margin Computation")

    generator = LinearDynamicalGenerator(vocab_size=8, hidden_dim=8, ortho_mode='random', seed=456)

    # Generate sequence
    result = generator.generate_sequence(length=6, start_token_id=0, mode='argmax')
    tokens = result['tokens']

    # Compute margins
    margins = generator.compute_margin(tokens)

    print(f"  Sequence length: {len(tokens)}")
    print(f"  Margins computed: {len(margins)}")
