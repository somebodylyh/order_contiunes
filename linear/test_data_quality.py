def test_deterministic_with_seed():
    """Test that same seed produces same results"""
    print("Test 6: Deterministic Behavior with Seed")

    # Generate twice with same seed
    gen1 = LinearDynamicalGenerator(vocab_size=16, hidden_dim=16, ortho_mode='random', seed=999)
    result1 = gen1.generate_sequence(length=10, start_token_id=0, mode='argmax')

    gen2 = LinearDynamicalGenerator(vocab_size=16, hidden_dim=16, ortho_mode='random', seed=999)
    result2 = gen2.generate_sequence(length=10, start_token_id=0, mode='argmax')

    tokens1 = result1['tokens']
    tokens2 = result2['tokens']

    print(f"  Run 1: {tokens1}")
    print(f"  Run 2: {tokens2}")

    assert tokens1 == tokens2, "Same seed should produce same sequence"

    print("  ✅ PASSED\n")


