import numpy as np
from collections import Counter
from data_generator import LinearDynamicalGenerator


def test_ortho_matrix():
    """Verify orthogonal matrix property: R^T @ R = I"""
    print("Test 1: Orthogonal Matrix Property")

    for mode in ['random', 'permutation']:
        generator = LinearDynamicalGenerator(vocab_size=16, hidden_dim=16, ortho_mode=mode, seed=42)
        R = generator.R

        # Check R^T @ R = I
        product = R.T @ R
        identity = np.eye(16)
        is_orthogonal = np.allclose(product, identity, atol=1e-5)

        print(f"  Mode '{mode}': Orthogonal = {is_orthogonal}")
        assert is_orthogonal, f"Matrix is not orthogonal in mode {mode}!"

    print("  ✅ PASSED\n")


def test_generate_sequence_basic():
    """Test basic sequence generation logic"""
    print("Test 2: Basic Sequence Generation")

    generator = LinearDynamicalGenerator(vocab_size=16, hidden_dim=16, ortho_mode='random', seed=42)

    # Generate a sequence
    result = generator.generate_sequence(length=10, start_token_id=0, mode='argmax')

    tokens = result['tokens']
    bag = result['bag']
    states = result['states']

    # Check length
    assert len(tokens) == 10, f"Expected length 10, got {len(tokens)}"

    # Check all tokens are valid
    for t in tokens:
        assert 0 <= t < 16, f"Invalid token {t}"

    # Check bag is consistent
    assert sum(bag.values()) == 10, "Bag count mismatch"
    for token in tokens:
        assert token in bag, f"Token {token} not in bag"

    # Check states length
    assert len(states) == 10, f"Expected 10 states, got {len(states)}"

    print(f"  Generated sequence: {tokens[:5]}... (length {len(tokens)})")
    print(f"  Bag: {dict(bag)}")
    print("  ✅ PASSED\n")


